# 07_string_methods_with_bang.rb

# Methods with bang
=begin
Bang (!) will modify the original object.
=end

puts "\n\n\n"

text = "Hello World"
puts "upcase: " + text.upcase
puts "original: " + text

text2 = "Habibun Hemel"
puts "upcase: " + text2.upcase!
puts "original: " + text2

text3 = "Another Example"
puts "reverse: " + text3.reverse!
puts "original: " + text3

text4 = "test case"
puts "capitalize: " + text4.capitalize!

puts "original: " + text4